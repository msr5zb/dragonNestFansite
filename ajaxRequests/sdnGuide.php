                                            <span style="color:blue"><b>Stage 7: Sea Dragon - Serpenta</b></span><br>

<br>
<br>
<b><span style="color:red">First Map: Phase 1 </span></b><br>
<br>
<iframe title="YouTube video player" width="480" height="390" src="https://www.youtube.com/embed/veDlWEpip4E" frameborder="0" allowfullscreen=""></iframe> <br>
<br>
<span style="color:blue"><b>HP x 20</b></span><br>
<br>
Upon going into this stage, Serpenta will cast dragon breath attack, which is almost one hit ko for most players. Quickly hide at the corner (see Video) to avoid the breath.<br>
<br>
<span style="color:blue"><b>Phase 1 skill:</b></span><br>
* Claw: Melee ranged<br>
* Middle Bite (Peck): Long ranged. Serpenta won't be able to hit you with this attack if you are hiding at the corner or stay by the side.<br>
* Dragon Breath: Will cast every 60sec once. Must avoid by hiding at the two corners. It has some sort of suction effect, so arrow key has to be hold down to avoid getting sucks in. <br>
<br>
<b>Dragon Breath is a special attack that can remove any buff on the target</b>. So nothing can save your from it (shield block, avatar, miracle etc). However, a special cooking item will make you immune to breath attack for a short duration. <br>
<br>
<span style="color:blue"><b>HP x19</b></span><br>
* Side Pecking: Serpenta will be able reach the corner at this phase. DPS char should see the motion of his head. Avoid swiftly if he is pecking into your direction. <br>
* Ice Claw: higher damage, slow effect.<br>
After the HP is brought down by 85% for this hp bar, Serpenta will fly to the next map.<br>
<br>
Before going into the second map, cast a highland on everyone. Miracle hand should be ready. <br>
<br>
=============================<br>
<br>
<b><span style="color:red">Second Map: Phase 2 - Cannon Phase</span></b><br>
<br>
Cannon View<br>
<iframe title="YouTube video player" width="480" height="390" src="https://www.youtube.com/embed/9zcAZP3-MWY" frameborder="0" allowfullscreen=""></iframe> <br>
<br>
Rear View<br>
<iframe title="YouTube video player" width="480" height="390" src="https://www.youtube.com/embed/vx5CrzqpPpc" frameborder="0" allowfullscreen=""></iframe> <br>
<br>
<span style="color:blue"><b>HP x 18.15</b></span><br>
<br>
Before entering this map, cast <b>highland</b> and <b>buff</b> up. <br>
<br>
Upon reaching the 2nd map, move to one of the <b>edge</b> of the map and <b>overlap</b> each other. Miracle hand should be cast immediately after that.<br>
<br>
The reason for overlapping is because ice flake will be cast on party member which has a eye icon. The ice flake will not hit the person with the eye icon and the person overlapping with him. However, ice flake will deal heavy damage to people around it.<br>
<br>
The reason for casting miracle hand is to reduce the damage from spike thrown by Serpenta. After his first dive breath, the 3 cannon holder can move to their respective cannons. <br>
<br>
Serpenta will be flying around in the sky during this phase. He can only be damaged by Cannons. Paladin can try to reflect the damage of spikes also. <br>
There are 5 cannons lying in line. It is advised that only the <b>first, third and fifth cannons </b>are used. leaving a gap of empty cannon between each cannons. The reason for doing so is to avoid the ice flakes (see below). <br>
<br>
<b>Boss Skills</b><br>
<br>
<br>
* <b>Ice Flakes (Eye): </b>Serpenta will mark a few player in the party. Dark eyes symbols will appear on those who are being marked. Serpenta will shoot ice flakes at those who are being marked. However, it won't damage the one being marked but only members around him, 50k ~ 70k damage, AOE, slow effect. Everyone should stand apart from those who are marked. Another method is stand exactly with the one being marked, overlapping each other.&nbsp;&nbsp;<br>
<br>
* Monster-throwing attack (<b>Spike</b>):&nbsp;&nbsp;Serpenta will throw monster from the sky to deal damage. Serpenta will never aim the person handling the cannon. To avoid getting hit by spikes, simply <b>move from your current location</b> when you see the <b>black smoke</b> on the dragon and<b> spikes</b> are thrown from him. Paladin should also provoke the monster so that they won't attack the person handling the cannons. <br>
<br>
* Ice meteor (<b>Cube</b>):&nbsp;&nbsp;Serpenta summon 5 ice meteors on the sky. They will grow and move towards the whole team slowly and explode. Damage is around 90k+, and extremely large AOE. The meteors can be destroyed with cannons (5-6 normal shot, 1 charged shot). In the unlikely event that the ice meteor can't be destroyed, it is recommended that all members run to the safe zone (see picture) to avoid the damage from ice meteor.<br>
<br>
Normally the party member who are not holding any cannons will be split into 2-3 subgroups, each at the side of the map. Move according to the skill cast: eye - overlap, spike - move around. <br>
<br>
After the dragon is brought down to near HPx16 (roughly 25%), the person handling the middle cannon should run and gather at the sides with the other party members. <br>
<br>
The other party members should be informed (they will not see the dragon's HP since they never hit it with cannon). Miracle hand should be cast and all party members overlap each other near the sides of the map.<br>
<br>
The other 2 members at the 1st and 5th cannon should observe the dragon's HP and its profile picture beside his HP carefully. Note that it normally glows red if the cannon hit the dragon. However, when the dragon's profile picture <b>STOP GLOWING RED </b>even if the cannon hit it, press ESC to release yourself from the cannon and dash to the sides of the map ASAP. The Dragon will dive down with a breath attack very shortly afterwards.<br>
<br>
NOTE #1: The dragon might not dive immediately at 16.25x HP. He might cast an additional wave of ice meteor which can prove dangerous if not dealt with. To prevent this, it is better to wait for additional rounds of ice meteor at 16.4x HP. After clearing this round of ice meteor, the 3rd cannoner can go to the side and overlap with others. The 1st and 5th cannoner will then continue hitting the dragon till the dragon dive down. <br>
<br>
NOTE #2: After it stop glowing red, <b>DO NOT run back to the cannon NO MATTER what happen</b>. <br>
<br>
=============================<br>
<br>
<b><span style="color:red">Phase 3 </span></b><br>
<br>
<iframe title="YouTube video player" width="480" height="390" src="https://www.youtube.com/embed/HTkfB-cVeDs" frameborder="0" allowfullscreen=""></iframe> <br>
<br>
Main-T stand near the edge of the map where the cannons previously were and start provoking the dragon. Sub-T stand beside him, roughly one cannon distance away. The other 6 party members should be divided into 2 sub-groups each at one side of the dragon. <br>
<br>
<b>Main-T &amp; Provoke</b><br>
Main T must keep the aggro on the dragon at all times and make sure the dragon is facing you. Provoke need to be level 7 at least with A/B duration plate. <br>
<br>
The timing for provoke is an art. It should not be spam whenever it is cooldown because the dragon is "immune" to provoke at several occasion.<br>
<br>
Most main-T will stay near the edge of the map to prevent Blackhole from being cast. While at the edge of the map, provoke should only be cast is when the dragon is<br>
* not doing anything<br>
* pecking sub-T or main-T<br>
* claw attack (with no ice)<br>
* doing tail swipe.<br>
* turning around when aggro on you is lost.<br>
* casting rotating dragon breath ( you will need to walk nearer to him)<br>
<br>
Provoke should not be cast when the dragon is:<br>
* casting ice claw<br>
* casting blackhole<br>
* flying and doing any airborne skills<br>
* casting ice age (bind)<br>
* casting rotating dragon breath (when you are at the edge of the map)<br>
<br>
Note: rotating dragon breath can only be provoked if you walk nearer to him. Staying at the edge of the map while casting provoke during this skills is useless. <br>
<br>
<br>
The two subgroups should gather at the tail of the dragon and deal damage under miracle hand. <br>
<span style="color:blue"><b>HP x 16.25</b></span><br>
* <b>Bite</b>: Serpenta will be using this throughout the whole battle. Normally only aimed at the paladins. <br>
* <b>Ice Claw</b>: homing effect, can be blocked with mirror. Will be cast throughout the whole battle.<br>
Paladin has to take note that the ice claw will cause <b>lost aggro</b>. Main-T or Sub-T has to refresh the aggro with provoke right after the ice claw attack. The key role of the two paladins is to make sure the dragon doesn't turn his head towards the other party members.<br>
<br>
<span style="color:blue"><b>HP x 15.25</b></span><br>
* <b>Dive Breath (Dive)</b>: Serpenta will flap his wings and fly to the sky. Shortly after that, he will dive down with a breath attack. Take note of which way his head is facing. Members from the two subgroups <b>hide at the two map edges at their respective edges (to the left or right of dragon) of the map</b> to avoid it. Main-T and Sub-T should run to the side too. <br>
<br>
<span style="color:blue"><b>HP x14.25</b></span><br>
* <b>Rotating dragon breath</b>: First cast at 14.25 x HP. Angle of rotation is roughly 180~270 degree, can be clockwise or counterclockwise. The screen will go blur for awhile while Serpenta flaps his wing. The blur effect induce a slow status on all party members. The breath attack that follows clear all buffs and deal very high damage. <br>
<br>
Mercenary and Paladin roles are very important here. <br>
<b>Mercenary:</b><br>
* Approach the dragon (near tail or hind legs) with <b>super armor buff</b> when the dragon's HP is near 14.25<br>
* Once the screen goes bluff, cast <b>Relieve</b> immediately which clears the slow effect.<br>
* Cast <b>Devastating howl</b> before the dragon flies up and start his breath attack. <br>
* If you miss <b>(no HP reduction observed),</b> <b>inform the paladin immediately</b> and the paladin will have to eat <b>dragon breath potion</b> to withstand the damage. <br>
<br>
<b>Paladin:</b><br>
* Cast elemental aura to reduce ice/water damage from the rotating breath (permanent aura effect cannot be removed by the breath). Although mercenary induce at 80% attack reduction on the dragon, the damage is still extremely high. Roughly 50k damage will be induced on the paladin under the combination of devastating howl + elemental aura. <br>
* Stay at your current location. Do not run to your other party members. If you manage to provoke before the casting of rotating breath, the dragon will not rotate at all. This is important because you certainly don't want your other party members' buffs, especially buff potion, to be cleared by the breath attack. <br>
* If devastating howl debuff is not applied to the dragon for some reason, eat <b>dragon breath potion</b> to be immune to the breath for 10sec. <br>
<br>
After the first rotating dragon breath at 14.25xHP, the next rotating dragon breath will be casted after <b>every 90second</b>. <br>
<br>
There are two types of Rotating Dragon Breath:<br>
(i) <b>HP-triggered</b><br>
* cast at 14.25, 9.25, 7.25 and 5.25 x HP. <br>
(ii) <b>Time-triggered</b><br>
* cast every 90sec after the first HP-triggered breath at 14.25. Will stop after hitting HPx10.5. <br>
<br>
Mercenaries have to make sure that their essential skills: <b>super armor buff</b>, <b>relief, and devastating howl </b>are ready before the rotating dragon breath. <br>
<br>
<span style="color:blue"><b>HPx 13.25</b></span><br>
*<b> Ice Age (Bind):</b> Serpenta will raise his head and flap his wings, freezing players. Player will die shortly afterwards. ~110k damage. Normally the person with the highest aggro will be frozen. From now onwards, Ice Age will be cast throughout the whole battle except 9.25~7.25x HP.<br>
<br>
Typically, that person is the main-T. If the main-T was frozen, the sub-T need to save him RIGHT AWAY with Smite. The main-T can prevent himself from being frozen by casting avatar or place summoned units (claw, hound or mushroom) in front of himself and take steps backwards. The frozen attack has a certain AOE and if the main-T is too close to the summoned units, he will be frozen as well. If the sub-T is a priest instead of paladin, relic should be cast in front of the main-T to act as sacrificial target. <br>
<br>
However, if the pally lose aggro and some other members are frozen, the other members in his sub-group should save him. Use <b>high super armor break</b> skills (miracle hand if need to) to&nbsp;&nbsp;break the ice. Try not to stand too near each other when ice age is cast so that the whole sub-group will not be frozen all together. <br>
<br>
Certain classes can summon sacrificial target to be frozen by ice age and thus preventing himself from being frozen. It is important to make sure that <b>you, your sacrificial target and the head of the dragon</b> are perfectly <b>in-line</b> and you are <b>not too close</b> to your sacrifice target. <br>
- Priest: all relics<br>
- Academic : all summons<br>
- Force user: mirror<br>
<br>
*<b> Black hole &amp; Tail Coil:</b> CD ~30sec. He will spit some dark matter which will suck whoever nearby to its center. Serpenta will then turn around his body and use hits tail to coil around anyone at the center of the dark matter, suffocating him. Extremely high damage.&nbsp;&nbsp;If you ever get sucked into the blackhole,<b> spam jump and tumble/blink</b> to have a slight chance to getting out. He will stop casting this after HPx7.25 <br>
