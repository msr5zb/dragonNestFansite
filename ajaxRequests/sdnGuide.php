<span style="color:blue"><b>HP x 9.25</b></span><br>
When the dragon flies, there are two possibilities: ice rain( purple bomb) or dive with breath attack. <br>
* <b>Ice Rain (Bomb)</b>: cleric just cast <b>shield block</b>. non-cleric have to <b>keep running</b> to avoid being damaged. Ice rain will be casted from 9.25x to 4.25x HP. It is important to note that <b>relics and summon units attract rain</b> also. When running, <b> DO NOT run towards relic and summon units or any of your party members.<br>
<br>
</b>NOTE: Ice Age will NOT appear from 9.25x to 7.25x HP. <br>
<br>
<b><span style="color:#663300">Feint-Phase</span></b><br>
<br>
<span style="color:blue"><b><span style="color:#6600cc">7.25X TO 4.25X HP</span><br>
<br>
</b></span>*<b> Ice Flakes:</b> A few members will be <b>marked with eyes</b>. 4 waves of ice flakes will be thrown down on some marked members. A small area within the ice flakes will be the safe zone. <br>
<br>
Sub-group members have to stand overlapped with the marked person at the side to avoid being damage. Has a small suction effect. Do not re-adjust your position with arrow key. <br>
<br>
Each of the 3 subgroups has to decide on a<b> fixed location</b> to run to and gather for overlapping. The locations are normally marked by geographical features for easy reference ( rock, hole etc) When the dragon flies, move to the spot immediately when eyes are spotted on party members. <br>
<br>
It is advised that the view should not be panned away from the dragon. The reason is, the dragon might cast ice rain when it flies, which requires you to run away from others, instead of gathering. <br>
<br>
*<b> Frontal Breath</b>: Follows immediately after the 4th wave of ice flake. The two subgroups at the side move further towards the back of the dragon slightly to avoid this breath. The tanker subgroup move the edge of the map (in the direction the dragon is facing). <br>
<br>
* <b>Dive Breath</b>: Follows immediately after the frontal breath. Hide at the edges as usual. <br>
<br>
The 3-combo: <b>Ice flake &gt; frontal breath &gt; dive </b>will always be cast together. The 3 combo will be cast from 7.25x to death.<br>
<br>
<b>* Side Breath:</b> This breath is extremely dangerous. He will cast this when he is on the ground, almost randomly. The breath spans from the front leg to the end of the tail. Therefore, it's very important NOT to stand any where near his sides after HPx6.25. The safe place to gather and deal damage is at the<b> TIP of the tail</b>. <br>
<br>
It is advised that, each ground, either miracle hand or devastating howl should be cast at the gathering point at the tail tip. When moving towards the tail tip, beware not to get too close to the side of the dragon. Try to move along the edge of the map, to be safe.<br>
<br>
<br>
