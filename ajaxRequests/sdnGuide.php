<br>
This is the most challenging phase of the whole dragon battle because the dragon will try to trick you with some false signals. <br>
<br>
Normally, eye is followed by ice flakes. The default action after seeing eye on party members , is to overlapped with your party members. However, after HPx6.25, after putting eye icon, he MIGHT start to cast ice rain instead of ice flake. If you stay overlap and not moving, you will be wiped out very soon.<br>
<br>
The key to solve this is to differentiate the casting animation between ice rain and ice flakes. Observe what he do after he flies.<br>
<br>
See this video to check out the casting animation :<br>
<iframe title="YouTube video player" width="480" height="390" src="https://www.youtube.com/embed/OKuroSMVYWE" frameborder="0" allowfullscreen=""></iframe> <br>
<br>
Video above - first part: <b>Direct Rain</b><br>
- He flied up and <b>open his arm wide</b> directly (with <b>NO</b> dark smoke at his claw). Rain will drop right away.<br>
<br>
Video above - Second part: <b>Eye (Fake) + Rain </b><br>
- He flied up and note that his arm is in front of him. At this moment, the "eye" icon will appear on a few party members. <br>
- However, this is a fake eye when his arm <b>OPEN WIDE </b>(with <b>NO</b> dark smoke at his claw). .He will cast rain instead of the normal flakes. <br>
<br>
<b>ADVANCED TECHNIQUE:</b><br>
* sometimes you probably don't have time to check the sky for&nbsp;&nbsp;his casting animation after he flies up , since you might be moving toward the overlapping point.<br>
* listen to the video above, and <b>memorize the sound</b> the dragon made when he is casting the ice rain.<br>
* once you can do that, you don't even need to check the sky to know that ice rain is coming. Very powerful technique once you have mastered it. <br>
<br>
<br>
* What you need to do in both cases above, is to cast your <b>shield block</b> if you have (should save for it) Or, if you don't have shield block, then just <b>keep moving.</b>... It 's a bit like Manticore flying bombing where he won't hit you if you keep moving.<br>
<br>
However, do not run into the other party members or tragedy will happen =.=&nbsp;&nbsp;Assign 3 paths for each members from each sub-group to run so that you guys won't run into each other. <br>
<br>
For flakes, his attack animation are described as follows:<br>
<br>
<b>Eye (Real) + Ice Flakes</b>:<br>
-&nbsp;&nbsp;After placing eye, if you see that <b>his mouth breath dark/icy breath</b> with <b>dark smoke on both claws. </b>After a<b> roar,</b> it will throw flakes.&nbsp;&nbsp;<br>
<br>
* You should quickly <b>move to the assigned overlap spot</b>.<br>
