                                            <font size="4"><b><u>Stage 6: Green Dragon</u></b></font><br />
<br />
<font size="3"><b><span style="color:#6600cc"><u>Phase 1:</u></span></b><br />
<br />
<iframe title="YouTube video player" width="480" height="390" src="https://www.youtube.com/embed/ETrSBy_fmZg" frameborder="0" allowfullscreen></iframe> <br />
<br />
</font><br />
<br />
After defeating the human form of Kalahan, and after the cutscene, everyone should move towards the black point as shown in the figure below, which is a safe spot for most of the dragon's attack.<br />
<br />
<img class="scrollLoading" href="https://img9.mmo.mmo4arab.com/newforum/upload//2012/04/10/1334050748844.png"> <br />
<br />
<br />
<b><span style="color:#6600cc">HPx20:</span></b><br />
* <b>Claw Attack :</b> dangerous , shown by bright red zone<br />
* <b>Claw Swipe</b>: shown by dark red zone<br />
After the claw swipe, the claw will be exposed outside the wall and <b>vulnerable for 10sec</b>. This is a good chance to deal damage. <br />
<br />
<br />
<b><span style="color:#6600cc">HPx19.25<br />
</span></b>The figure above show the AOE for the 3 types of dragon breaths:<br />
<b>Poison Breath</b> : very dangerous, Green zone<br />
<b>Fire Breath</b> : very dangerous, Red zone<br />
<b>Ice Breath</b> :&nbsp;&nbsp;blue zone. Some ice will appear on his head.<br />
<br />
Note that only Ice breath will hit the party members if they stay at the recommended spot. (the lower left corner). So it is recommend that you are familiar with the casting motion (watch for the ice on his head) of ice breath. You can avoid the ice breath by moving to the green or red zone. Under most circumstance, ice breath will not kill any of the party members if paladin has elemental aura and priest cast buff. <br />
<br />
<b>Summon Comet: </b>The casting of this skill is very similar to the rotating dragon breath of sea dragon. It will start with a screen blurring effect. After that, the AOE of the skill covers almost the whole screen, except near the opening.&nbsp;&nbsp;All party members can get close to the dragon at the opening between the walls and<b> deal damage safely for ~10sec</b>. Run back to the lower left corner again after this attack ended. <br />
<br />
<b>IMPORTANT NOTE:</b><br />
- Observe the order of skill casting after HP x19.25. The pattern will repeat itself after the Summon Comet.<br />
- For example, if the dragon use claw &gt; poison &gt; ice &gt; flame &gt; summon comet. After the summon comet, it will definitely cast claw &gt; poison &gt; ice &gt; flame &gt; summon comet again in this order. <br />
- At 17.25x HP, dragon will rush out from the opening and enter phase 2. Weaker class will suffer heavy damage if stand near the opening. <br />
<br />
<br />
<b>Activation of Heraldic Device</b><br />
<br />
Throughout stage 1 to 5 of GDN, you can collect this item called &quot;<b>Cleansing Totem</b>&quot; after defeating the bosses.<br />
<br />
They (you have to collect all 5) can be used here on to activate the Heraldic Device (location shown in the picture below).<br />
<br />
<b>IMPORTANT NOTE:</b><br />
1. Do NOT go towards the device immediately after the words 'Heraldic Device can be activated'. <br />
2. Doing so will most likely kill you since you might get hit by Poison Breath<br />
3. Make sure the dragon is not about to cast Poison Breath before moving towards the device.<br />
<br />
Activating the Heraldic device gives you <b>15sec of invincibility</b>. Deal damage to the dragon as much as possible during this time frame. <br />
<br />
However, most party normally ignore this totem. <br />
<br />
<br />
<b><font size="3"><span style="color:#6600cc"><u>Phase 2</u></span></font></b><br />
<br />
<img class="scrollLoading" href="https://img9.mmo.mmo4arab.com/newforum/upload//2012/04/10/1334051874938.png"> <br />
<br />
After the HP reach x17.25, the dragon will break the wall and your party members <b>have to run to the Phase 2 Chamber B</b>, as shown in the picture above. Do so quickly or be killed by the dragon. <b>DO NOT STOP at Chamber A</b> or be killed. Speed-buffer helps. <br />
<br />
A lot of monsters will appear in Chamber B. The dragon, which stays in Chamber A, will not attack you during this phase. However you only have <b>40sec to clear all the monsters in this room</b>, or else the whole party will die immediately.<br />
<br />
If you manage to clear all the monster within 40sec, the dragon will chase after you, and you will need to move to Chamber C. A lot of monsters will appear here again., and you must clear them all within 40sec or face total annihilation. <br />
<br />
<br />
After that, the party members will be teleported to a large area in phase 3 of the battle. <br />
<br />
<font size="4"><span style="color:#6600cc"><b><u>Phase 3:</u></b></span></font><br />
<br />
<iframe title="YouTube video player" width="480" height="390" src="https://www.youtube.com/embed/fzARkDCJjwo" frameborder="0" allowfullscreen></iframe> <br />
<br />
<br />
<img class="scrollLoading" href="https://img9.mmo.mmo4arab.com/newforum/upload//2012/04/10/1334052524100.png"> <br />
<br />
The battle will continue in a relatively large area, as shown below. The dragon will be in the middle as shown.<br />
<br />
There are 4 heraldic towers at the 2 edges of the map as shown (Marked by 1 to 4, in the order of activation) .<br />
<br />
After going into this map, all party members (With Holy Shield) should move immediately to 'Tail Right', which is relatively safe.<br />
<br />
Paladin on the other hand, should provoke and stay in front of the dragon until all the party members move safely to 'Tail Right'. <br />
<br />
<br />
<b><span style="color:#6600cc">Boss Basic Attack start from HP x17:</span></b><br />
<br />
<b>3 Hits Tail Swipe</b>: Dangerous. Use the tail to smack the ground, and swipe left and right. The dragon will not cast it if Paladin is provoking the dragon in front. <br />
<b>Bite</b>: Only hit a small frontal area. <br />
<b>Claw Swipe</b>: Similar to phase 1. <br />
<b>Stomp</b>: Raise 2 of its front leg high. Has aftershock damage. Will do a tail swipe in the frontal direction. Very dangerous.<br />
<b>Wing Attack:</b> Dangerous. Raise its legs and flap wing continuously. AOE is around its belly. Anyone who are staying in front of the hind legs to the belly, will be push towards the dragon head and receive heavy damage. A good chance to deal damage at its tail or behind the hind legs. The dragon will not cast this if paladin is provoking the dragon in front.<br />
<br />
<b><span style="color:#6600cc">New Boss Attack start from HP x15.25:</span></b> <br />
<br />
<b>Poison Breath</b>: Very Dangerous. The spine of dragon will <b>glow green</b>. There are two variations of Poison Breath: fan-shaped - hits the area around his front claw when he turn his head. The other one, Frontal Straight Breath will only be casted after 8.25x or so.&nbsp;&nbsp;Both poison breath only hit area in front, and since most of the time, we are at the hind leg, it's safe. therefore whenever you see the dragon glow green, it literally gives you the green light to deal damage safely at the hind legs. <br />
<br />
<b>Toxic Spray:</b> Spine of the dragon will <b>glow green. </b>The dragon will raise his head and roar. A lot of toxic spray will also be thrown into the air. Negligible, a good time to deal damage. <br />
<br />
<b>Flame Tornado:</b> Very Dangerous. The spine of the dragon will<b> glow red</b> before casting. His left front leg will glow red and rise above ground before cast. Flame tornado will appear at an area spanning from his front claw to the tail. It can be blocked with relics/mirrors. Stay at the<b> edge of the map behind the boss/end of tail</b> is safe too. You can also hide in front of the dragon near the left/right edge of the map, but you will be vulnerable to poison breath. Can be blocked with shield block/avatar. <br />
<br />
<b>Magic Eye:</b> The spine of the dragon will <b>glow white</b> before casting; his left front leg will glow white/blue and rise above ground before cast. Some of the party members will be marked with eye. EVERYONE must overlap with the marked members, or suffer heavy damage (might even 1hit KO). Suggest <b>overlapping at the tail left/right </b>(see Figure above). A good chance to deal high damage to the boss while overlapping. Beware if relics/summon units are marked, must overlap with them if it happens. Quickly disperse when this attack is over or else, everyone might be bound by vine.&nbsp;&nbsp;<br />
<br />
<br />
<span style="color:#6600cc"><b>Hp x 15.25, 12.25, 9,25, 6.25: Destroying Heraldic Towers</b></span><br />
<br />
At HP x15.25, the spine of the dragon will <b>glow green</b>. The word 'Destroy Heraldic Tower' will appear on the screen. The<b> screen blurring effect clear all buffs and induce &quot;slow&quot; effect on all members.</b> Priest should use Curing Relic and refresh buff; Academic/Acrobat should cast speedup buff. <br />
<br />
All party members should go to <b>Tower 1</b> and use high super armor break skill to <b>destroy the tower immediately</b>. The towers are activated in the order shown by the picture above (1 to 4, at HPX15.25, 12.25x, 9.25x, 6.25x respectively)<br />
<br />
If the party fail to do so within a specific time, the dragon will fly and cast a poison breath, which will kill the whole party immediately. <br />
<br />
If the party manage to destroy the tower, an <b>invincible buff</b> (green icon) will be granted to the whole party when everybody get into the platform at the middle (see figure below). Also, take this opportunity to <b>deal damage to the dragon as much as possible before it attempts to fly. </b><br />
<br />
When the dragon fly up, every party member should <b>gather at the platform at the middle</b> (see the figure above) which will glow after the tower has been destroyed. Failure to do so, will again cause total annihilation.&nbsp;&nbsp;The poison breath will hit the whole field, except this platform.<br />
<br />
After gathering at the middle and obtain the special protection buff, the whole party members should move to the left or right edges of the map. If P1 is activated, move to the 'Sky Right'. If P2, P3 or P4 is activated, move to 'Sky Left' (see schematic above)<br />
<br />
After finishing the Poison Breath annihilating attack, the dragon will stay at the edge of the map for a while. All party members must go the edge of the map and observe what the dragon is doing. He can cast 1 or more of the following skills while in air at the edge of the map:<br />
<br />
<b><span style="color:#6600cc">Boss Air Skills starting from HP x15.25</span></b><br />
<br />
<b>Gigantic Flame Ball</b>: Extremely dangerous. The dragon spine/crest will glow <b>red</b>. A huge flameball will be gathered at his claws in the middle of the field (red zone in the figure below). The flame ball will be thrown, hitting the whole field. Most members will be 1-hit KO, unless they hide near the cliff (At the edge of the map). <br />
<br />
<b>Gigantic Ice Ball:</b> Extremely dangerous. The casting motion is similar to flame ball, except that the color is <b>white.</b> It hits the front half of the area (Atop at about 2m from the 1st pillar). Safe zone are marked by the blue zone in the figure below. <br />
<br />
The dragon will <b>land for a few second after casting fire / ice ball.</b> It's a good chance to deal some damage to him at Point B (see figure below), before he is airborne again. <br />
<br />
<b>Summon Monsters:</b> Only appear after Hp x11.25. Summon a few monsters (Boss in Islah Village). Move back to the sky spot left/right if fireball is not casted yet. <br />
<br />
<b>Poison Dive Breath: </b>Can be avoided easily by staying at the edges of the map.<br />
<br />
<br />
<b><span style="color:#6600cc">New Boss Attack start from HP x12.25</span></b><br />
<br />
<b>Vine Grip:</b>&nbsp;&nbsp;It will <b>raise his right front leg and the tail high to the right </b>before casting. When the tail/leg hit the ground, some party members will be bound with vine. The color of the spine is neutral (neither green,red or white).&nbsp;&nbsp;All party members should <b>disperse immediately</b>. If any of the party members are bound, <b>save</b> him similar to the Ice Bind of Sea Dragon. <br />
Can be avoided with<b> jumping at the moment when the tail/leg smack the ground</b>. Use skills which give airborne time is a good way to evade it.&nbsp;&nbsp;<br />
Very high FIXED damage if you are bound (first hit 55k, subsequent tick per sec is 3000 for 10sec, final hit 130k.). Need about 210k+ hp to survive. <br />
<br />
<br />
<b><span style="color:#6600cc">New Boss Attack start from HP x9.25 </span></b><br />
<b><br />
Summon Twin Skeleton Minotaurs:</b> First appear after HP x9.25. The dragon crest will turn purple and<b> fly up to the sky vertically</b>. After 10sec, the dragon will summon two skeleton: One red and one blue. The horn of the dragon will change color (blue or red) after another 10sec. The bull with the same color as the dragon at this moment, need to be killed within 15sec, or else the total annihilation skill will wipe the whole party. If the party manage to kill the correct bull within 15sec, a magic circle will appear at that location, which allows the party members to hide safely from the annihilation attack. <br />
<br />
<b>IMPORTANT NOTE:</b><br />
- When one bull is killed, the other one will fade away.<br />
- You can kill both bulls at the same time<b>.</b> This is not recommended because the party can mess up the killing time and the correct color bull fade away<b>. </b>Normally, a Mercenary will use Taunting Howl to lure one of the bull away, separating them.<br />
- Make sure you <b>stand in the magical circle with the right color.</b> <br />
- Make sure no Curing Relic on field when everybody gather in the magical circle. Curing Relic will clear the invincible buff and cause total annihilation.<br />
- Mercenary use super armor buff, just in case there are some mobs on the field trying to push you out of the circle. <br />
- When the dragon land after this attack, there will be an aftershock damage. Jump to avoid. <br />
- Mercenary should use Devastating Howl after the dragon land if the HP has yet go into 5.25. Reason: Flame Tornado might kill others who are running towards the tail. <br />
<br />
<br />
<b><span style="color:#6600cc">New Boss Attack start from HP x 5.25</span></b><br />
Dragon will not cast Fire Tornado and Magic Eye after x5.25. <br />
<br />
<b>Scarab Swarm:</b> Cast after 5.25xHP. Extremely dangerous. The boss will beat his wings repeatedly for roughly 10sec while staying on the ground, before taking flight. Scarab Swarm will appear at every party member's feet.&nbsp;&nbsp;This attack<b> clear buff </b>and induce a very powerful poison status which cannot be cured for 12sec<b>. </b>There are 2 suggestions:<br />
*&nbsp;&nbsp;Use Devastating Howl after 10sec after wing flapping , just before the dragon fly. <br />
*&nbsp;&nbsp;Try to<b> break the super armor bar of the dragon by attacking his head</b>. If the super armor bar is depleted in time, the dragon will faint - A good time to deal damage!<br />
<br />
If both measures fail, staying at the frontal/back area of the dragon make it easier to avoid this attack, due to larger running area. Keep moving to avoid this skills (Pretty much like bull charging of stage 5 in SDN).<br />
<br />
